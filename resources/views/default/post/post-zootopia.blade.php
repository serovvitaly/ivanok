            <div class="head2 title">О чем мультфильм?</div>
            <p>Сюжет и смысл – разные вещи, поэтому на данный вопрос мы считаем нужным дать отдельный ответ. «Зверополис» - это мультфильм не о зверях и отношениях между ними, а о жизни. Джуди Хоппс проводит исследование ситуации, анализирует текущее положение дел и выясняет, что население города на 90% состоит не из хищников, а из их потенциальных жертв – маленьких и безобидных зверьков, которые в естественных природных условиях никак не могут быть на стороне агрессивного, кровожадного меньшинства, которое в любой голодный момент способно перегрызть их. Но интересно в данном случае другое – в самых мирных условиях такое большинство все равно боится меньшинства, что придает мультфильму глубокое социальное звучание.</p>

            <p>Диснеевский мультфильм демонстрирует нам, что бывает, когда разные люди и культуры вынуждены взаимодействовать в тесном пространстве – и картина далеко не всегда получается мирной, дружелюбной и счастливой. Предубеждения – вот причина появления пропасти между людьми, а предубеждений в нашем обществе очень и очень много. Они рождаются из агрессии и могут выражаться открыто, но чаще предрассудки вырастают из страха или вообще на ровном месте.</p>

            <p>Идея: следуй за мечтой – достигай целей!
            Популярности мультфильма «Холодное сердце» можно только позавидовать, но она возникла не на ровном месте. Все дело в том, что сценаристы киностудии отошли от привычной сказочной схемы из серии «Следуй за путеводной звездой – и она приведет тебя к успеху». Все в жизни совсем не так, о чем речь идет и в «Холодном Сердце», и в «Зверополисе». Чтобы мечты сбывались, нужно предпринимать соответствующие действия – только упорный труд способен помочь нам в достижении целей.</p>

            <div class="head2 title">Техническое совершенство</div>
            <p>
            С технической точки зрения мультфильм сделан прекрасно. Судите сами:
            <ul>
                <li>Максимальная зрелищность, красивые спецэффекты.</li>
                <li>Животные, которые выглядят настоящими, живыми.</li>
                <li>Динамичное развитие сюжета.</li>
                <li>Качественная озвучка и просто хороший звук.</li>
                <li>Четкое изображение, которое позволит рассмотреть каждую мелкую деталь – будь то виток на фонарном столбе или цветок в окне.</li>
            </ul>
            Этого всего достаточно, чтобы подарить максимальное эстетическое удовольствие от просмотра. Мораль – это важно, но качественное исполнение играет не меньшее значение.
            </p>
            <p>Ищете интересный мутильтик для семейного просмотра? Обязательно посмотрите «Зверополис». Он понравится самым маленьким своей зрелищностью, а детишкам постарше преподнесет важный жизненный урок. И это все под заводную песню в исполнении Шакиры!</p>

