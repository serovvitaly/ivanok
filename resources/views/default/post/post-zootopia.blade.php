            <p>Крольчиха Джуди после одного неоднозначного случая понимает, что лис Ник Уайлд, который совершил побег из тюрьмы (где он, правда, сидел по ошибке) – мошенник. Но все ли так линейно-понятно и просто? Конечно, нет, и у Хоппс рождается план. Для начала она просит лиса посодействовать ей в поиске без вести пропавшей выдры – в конце концов, именно лис видел животное последним перед его исчезновением. Далее сюжет начинает закручиваться в детективном русле – и рождается увлекательная семейная картина, просмотр которой будет не скучным не только для маленьких деток, но и для их старших братьев-сестер, родителей и даже бабушек-дедушек. Естественно, детективная история выполнена в стиле Диснея – красиво, сказочно и все-таки по-доброму.</p>

